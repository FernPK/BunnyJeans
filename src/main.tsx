import React from 'react'
import ReactDOM from 'react-dom/client'
import App from './App.tsx'
import './index.css'
import {
  createBrowserRouter,
  RouterProvider,
} from "react-router-dom";
import Home from './routes/Home.tsx';
import Collections from './routes/Collections.tsx';
import ItemList from './routes/ItemList.tsx';
import ItemDetail from './routes/ItemDetail.tsx';
import Basket from './routes/Basket.tsx';
import Wishlist from './routes/Wishlist.tsx';

const router = createBrowserRouter([
  {
    path: "/",
    element: <App />,
    children: [
      {
        path: "/",
        element: <Home />,
      },
      {
        path: "/collections",
        element: <Collections />,
      },
      {
        path: "/collections/:collectionName",
        element: <ItemList />,
      },
      {
        path: "/products/:productId",
        element: <ItemDetail />,
      },
      {
        path: "/basket",
        element: <Basket />,
      },
      {
        path: "/wishlist",
        element: <Wishlist />,
      }
    ],
  },
]);

ReactDOM.createRoot(document.getElementById('root')!).render(
  <React.StrictMode>
    <RouterProvider router={router} />
  </React.StrictMode>,
)
